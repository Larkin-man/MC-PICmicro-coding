#include<p16f84.inc>
 ORG 0
 GOTO BEGIN
 ORG 100h
BEGIN

 CODE1 EQU 0Ch
 CODE2 EQU 0Dh
 CODE3 EQU 0Eh
 CODE4 EQU 0Fh
 CODE5 EQU 010h
 VAR3 EQU 011h
 VAR4 EQU 012h
 
MAINLOOP

 BCF STATUS,RP0
 CLRF PORTB
 MOVLW B'00000000'
 BSF STATUS,RP0
 MOVWF TRISB

 BCF STATUS,RP0
 CLRF PORTA
 MOVLW B'11111'
 BSF STATUS,RP0
 MOVWF TRISA
 BCF STATUS,RP0

 MOVLW B'10000'
 MOVWF CODE1
 MOVLW B'01000'
 MOVWF CODE2
 MOVLW B'00100'
 MOVWF CODE3
 MOVLW B'00010'
 MOVWF CODE4
 MOVLW B'00001'
 MOVWF CODE5

 MOVF PORTA,0
 XORWF CODE1,1
 BTFSC STATUS,Z
 CALL L1

 MOVF PORTA,0
 XORWF CODE2,1
 BTFSC STATUS,Z
 CALL L2

 MOVF PORTA,0
 XORWF CODE3,1
 BTFSC STATUS,Z
 CALL L3

 MOVF PORTA,0
 XORWF CODE4,1
 BTFSC STATUS,Z
 CALL L4
GOTO MAINLOOP

L1
 MOVLW H'A7'
 MOVWF PORTB
 MOVF CODE5,0
 XORWF PORTA,0
 BTFSS STATUS,Z
 GOTO L1
RETURN

L2
 MOVLW D'25'
 ADDLW D'78'
 MOVWF PORTB
 MOVF CODE5,0
 XORWF PORTA,0
 BTFSS STATUS,Z
 GOTO L2
RETURN

L3
 MOVLW 05h
 MOVWF VAR3
 MOVLW 0h
 M1
 ADDLW D'10'
 DECFSZ VAR3,1
 GOTO M1
 MOVWF VAR3
 LL3
 MOVF VAR3,0
 MOVWF PORTB
 MOVF CODE5,0
 XORWF PORTA,0
 BTFSS STATUS,Z
 GOTO LL3
RETURN

L4
 MOVLW 0h
 MOVWF PORTB
 MOVLW D'255'
 MOVWF VAR4
 MOVLW D'3'
 M2
 INCF PORTB,1 
 SUBWF VAR4,1
 BTFSS STATUS,Z
 GOTO M2
 LL4
 MOVF CODE5,0
 XORWF PORTA,0
 BTFSS STATUS,Z
 GOTO LL4
RETURN
 
END
