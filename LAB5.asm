#include<p16f84.inc>
 ORG 0
 GOTO BEGIN
 ORG 100h
BEGIN

SCRATCH EQU 0Ch

 BCF STATUS,RP0
 CLRF PORTB
 MOVLW B'00000000'
 BSF STATUS,RP0
 MOVWF TRISB
 BCF STATUS,RP0

 MOVLW 0h
 MOVWF PORTB
 MOVLW D'200'
 MOVWF SCRATCH
 MOVLW D'4'

LOOP
 INCF PORTB,1 
 SUBWF SCRATCH,1
 BTFSS STATUS,Z
 GOTO LOOP

 GOTO $
 END
