#include<p16f84.inc>

INITB 	EQU B'00000000'
VARA 	EQU 0Ch
VARB 	EQU 0Dh

ORG 0
GOTO BEGIN
ORG 100h

BEGIN
	BCF 	STATUS, RP0
	CLRF 	PORTB
	MOVLW 	INITB
	BSF 	STATUS, RP0
	MOVWF 	TRISB

	BCF 	STATUS, RP0

	MOVLW	D'50'
	MOVWF	VARA
	MOVLW	D'25'
	MOVWF	VARB

	MOVF 	VARB, 0
	CLRF 	VARB
LOOP
	SUBWF	VARA, 1
	INCF	VARB, 1
	INCF	VARA, 1
	DECFSZ	VARA, 1
	GOTO 	LOOP

	MOVF	VARB, 0
	MOVWF	PORTB
END
