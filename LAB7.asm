#include<p16f84.inc>
 ORG 0
 GOTO BEGIN
 ORG 100h
BEGIN

SCRATCH EQU 0Ch
DIGIT EQU 0Dh 
REG EQU 0Eh

 BCF STATUS,RP0
 CLRF PORTB
 MOVLW B'00000000'
 BSF STATUS,RP0
 MOVWF TRISB
 BCF STATUS,RP0

 MOVLW B'00000011'
 
ONE
 CALL PAUSE
 MOVWF PORTB
 ADDWF PORTB,1
 MOVF PORTB,0
 GOTO ONE
 

PAUSE ; Частота мигания 1Гц
 MOVLW D'255'
 MOVWF DIGIT
THREE
 MOVLW D'255'
 MOVWF SCRATCH
FOUR
 DECFSZ SCRATCH,1
 GOTO FOUR
 DECFSZ DIGIT,1
 GOTO THREE
 RETURN

 END
