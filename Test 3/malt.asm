#include<p16f84.inc>
NUMBER1 EQU B'00001100'  ;12
NUMBER2 EQU B'00000011'  ;3
ADDONREG EQU 0Ch
ADDONREG2 EQU 0Dh
;INITA EQU D'0'
INITB EQU D'0'

ORG 0
GOTO BEGIN
ORG 100h

BEGIN

BCF STATUS,RP0
CLRF PORTB
MOVLW INITB
BSF STATUS, RP0
MOVWF TRISB

BCF STATUS, RP0

MOVLW NUMBER1
MOVWF ADDONREG

;MOVLW B'00000000'
CLRW
CLRF ADDONREG2

ADDLW NUMBER2
START
;ADDLW NUMBER1
INCF ADDONREG2
SUBWF ADDONREG,1
INCF ADDONREG,1
DECFSZ ADDONREG,1
GOTO START

;ADDLW B'00000001'
MOVF ADDONREG2,0
MOVWF PORTB

END
 
